const $ = (s) => document.querySelector(s);
const esc = (s) => String(s).replace(/[&<>"']/g, c => ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[c]));
const recipes = rawRecipes.trim().split(/\n---\n/).map(block => {
  const [head, ...lines] = block.split('\n');
  const [title, category, tags=''] = head.split('|');
  return { title: title.trim(), category: category.trim(), tags: tags.split(',').map(t=>t.trim()).filter(Boolean), body: lines.join('\n').trim() };
});
const state = { q: '', cat: 'All' };
function text(r){ return [r.title,r.category,r.tags.join(' '),r.body].join(' ').toLowerCase(); }
function cats(){ return ['All', ...[...new Set(recipes.map(r=>r.category))].sort()]; }
function renderChips(){ $('#chips').innerHTML = cats().map(c => `<button class="chip ${c===state.cat?'active':''}" data-cat="${esc(c)}">${esc(c)}</button>`).join(''); }
function filtered(){ const q=state.q.trim().toLowerCase(); return recipes.filter(r => (state.cat==='All'||r.category===state.cat) && (!q || text(r).includes(q))); }
function render(){ const rows=filtered(); $('#count').textContent = `${rows.length} recipe${rows.length===1?'':'s'}`; $('#list').innerHTML = rows.length ? rows.map(r => `<details class="card"><summary><div><h2 class="title">${esc(r.title)}</h2><div class="meta"><span class="badge">${esc(r.category)}</span>${r.tags.slice(0,4).map(t=>`<span class="badge">${esc(t)}</span>`).join('')}</div></div><span class="chev">⌄</span></summary><div class="body"><h3>Recipe</h3>${esc(r.body)}</div></details>`).join('') : '<div class="empty">No recipes found. Try another search.</div>'; }
